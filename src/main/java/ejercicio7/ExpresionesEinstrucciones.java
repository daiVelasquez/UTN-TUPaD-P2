package ejercicio7;

/**
Analiza el siguiente código y responde: ¿Cuáles son expresiones y cuáles son 
instrucciones? Explica la diferencia en un breve párrafo.  
int x = 10; // Línea 1  
x = x + 5; // Línea 2  
System.out.println(x); // Línea 3 
 */
public class ExpresionesEinstrucciones {
    public static void main(String[] args){
        
        int x = 10; //Es una instrucción de declaración´+ asignación
        
        x = x + 5;  //Es una Intrucción de asignación.  
    
       System.out.println(x); //Es una instrucción de llamada al método.
    }
}

/*Una Expresión es algo que produce un valor, mientras que una instrucción es una
orden completa que la computadora ejecuta*/ 